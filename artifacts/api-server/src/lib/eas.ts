const BINARY_EXTENSIONS = new Set([".png", ".jpg", ".jpeg", ".webp", ".gif", ".ico", ".bmp", ".tiff", ".ttf", ".otf", ".woff", ".woff2"]);

/**
 * Returns true for file paths that should be treated as binary.
 * Also returns true when the content itself is a base64 data URI.
 */
function isBinaryFile(filePath: string, content: string): boolean {
  if (BINARY_EXTENSIONS.has(extname(filePath).toLowerCase())) return true;
  if (content.startsWith("data:") && content.includes(";base64,"))  return true;
  return false;
}

/**
 * Extract raw base64 string from a data URI (data:image/png;base64,<HERE>)
 * or return the content unchanged if it is already a plain base64 string.
 */
function toBase64(content: string): string {
  const idx = content.indexOf(";base64,");
  return idx !== -1 ? content.slice(idx + 8) : content;
}

async function writeTempProject(files: Record<string, string>): Promise<string> {
  const dir = await mkdtemp(join(tmpdir(), "nexus-build-"));
  // Resolve the canonical temp root so path-traversal attempts can be detected.
  const root = resolve(dir) + sep;

  for (const [filePath, content] of Object.entries(files)) {
    const full = resolve(join(dir, filePath));

    // Containment check: reject any path that escapes the temp directory.
    if (!full.startsWith(root)) {
      console.warn(`[EAS] Skipping unsafe path: ${filePath}`);
      continue;
    }

    await mkdir(dirname(full), { recursive: true });

    if (isBinaryFile(filePath, content)) {
      // Write binary assets as a Buffer so the file is not corrupted by UTF-8 encoding.
      await writeFile(full, Buffer.from(toBase64(content), "base64"));
    } else {
      await writeFile(full, content, "utf8");
    }
  }
  console.log(`[EAS] Wrote ${Object.keys(files).length} files to ${dir}`);
  return dir;
}

async function runEasCli(
  args: string[],
  cwd: string,
  timeoutMs = 300_000,
): Promise<string> {
  const token = process.env.EXPO_TOKEN;
  if (!token) throw new Error("EXPO_TOKEN not set");

  console.log(`[EAS] Running: eas ${args.join(" ")} (cwd=${cwd})`);
  const { stdout, stderr } = await execFileAsync(EAS_BIN, args, {
    cwd,
    timeout: timeoutMs,
    env: {
      ...process.env,
      EXPO_TOKEN: token,
      CI: "1",
      EXPO_NO_TELEMETRY: "1",
    },
  });
  if (stderr) console.log(`[EAS stderr] ${stderr.slice(0, 500)}`);
  return stdout;
}

async function pushFilesToGitHub(
  repoName: string,
  files: Record<string, string>,
): Promise<string> {
  try {
    const { ReplitConnectors } = await import("@replit/connectors-sdk");
    const connectors = new ReplitConnectors();

    async function gh(endpoint: string, options: RequestInit = {}): Promise<any> {
      const res = await connectors.proxy("github", endpoint, options as any);
      const text = await res.text();
      if (!res.ok) throw new Error(`GitHub ${res.status} on ${endpoint}: ${text.slice(0, 300)}`);
      try { return JSON.parse(text); } catch { return text; }
    }

    try {
      await gh(`/repos/${GITHUB_OWNER}/${repoName}`);
    } catch {
      await gh("/user/repos", {
        method: "POST",
        body: JSON.stringify({ name: repoName, private: true, auto_init: true, description: "Generated by NexusElite AI Studio" }),
        headers: { "Content-Type": "application/json" },
      });
      await new Promise(r => setTimeout(r, 2500));
    }

    const repoData  = await gh(`/repos/${GITHUB_OWNER}/${repoName}`);
    const branch    = repoData.default_branch ?? "main";
    const refData   = await gh(`/repos/${GITHUB_OWNER}/${repoName}/git/ref/heads/${branch}`);
    const commitSha = refData.object.sha;
    const commitData = await gh(`/repos/${GITHUB_OWNER}/${repoName}/git/commits/${commitSha}`);
    const treeSha   = commitData.tree.sha;

    const newTree = await gh(`/repos/${GITHUB_OWNER}/${repoName}/git/trees`, {
      method: "POST",
      body: JSON.stringify({
        base_tree: treeSha,
        tree: Object.entries(files).map(([path, content]) => {
          if (isBinaryFile(path, content)) {
            // GitHub requires base64-encoded content + explicit encoding for binary blobs.
            return { path, mode: "100644", type: "blob", content: toBase64(content), encoding: "base64" };
          }
          return { path, mode: "100644", type: "blob", content };
        }),
      }),
      headers: { "Content-Type": "application/json" },
    });

    const newCommit = await gh(`/repos/${GITHUB_OWNER}/${repoName}/git/commits`, {
      method: "POST",
      body: JSON.stringify({ message: "Generated by NexusElite AI Studio", tree: newTree.sha, parents: [commitSha] }),
      headers: { "Content-Type": "application/json" },
    });

    await gh(`/repos/${GITHUB_OWNER}/${repoName}/git/refs/heads/${branch}`, {
      method: "PATCH",
      body: JSON.stringify({ sha: newCommit.sha, force: true }),
      headers: { "Content-Type": "application/json" },
    });

    console.log(`[EAS] Pushed ${Object.keys(files).length} files to GitHub: ${GITHUB_OWNER}/${repoName}`);
    return `https://github.com/${GITHUB_OWNER}/${repoName}`;
  } catch (err) {
    console.warn(`[EAS] GitHub push failed (non-fatal): ${err}`);
    return "";
  }
}

export async function triggerMobileBuild(opts: {
  projectId:   string;
  projectName: string;
  platform:    "android" | "ios" | "all";
  files:       Record<string, string>;
}): Promise<MobileBuildResult[]> {
  const { projectId, projectName, platform, files } = opts;
  const repoSlug = `nexus-mobile-${projectId}`.slice(0, 60);

  const [dir, repoUrl] = await Promise.all([
    writeTempProject(files),
    pushFilesToGitHub(repoSlug, files),
  ]);

  console.log(`[EAS] Triggering ${platform} EAS build for "${projectName}"…`);

  const output = await runEasCli(
    [
      "build",
      "--platform", platform,
      "--profile", "preview",
      "--non-interactive",
      "--no-wait",
      "--json",
    ],
    dir,
  );

  let builds: any[] = [];
  try {
    const parsed = JSON.parse(output.trim());
    builds = Array.isArray(parsed) ? parsed : [parsed];
  } catch {
    const idMatch = output.match(/"id"\s*:\s*"([^"]+)"/);
    builds = [{ id: idMatch?.[1] ?? "unknown", status: "in-queue", platform: platform === "all" ? "android" : platform }];
  }

  return builds.map((build: any) => {
    const buildId = build.id ?? build.buildId ?? "unknown";
    const resolvedPlatform: "android" | "ios" =
      String(build.platform ?? "").toLowerCase().includes("ios") ? "ios" : "android";
    console.log(`[EAS] Build queued: ${buildId} (${resolvedPlatform})`);
    return {
      buildId,
      status:      build.status ?? "in-queue",
      platform:    resolvedPlatform,
      artifactUrl: build.artifacts?.applicationArchiveUrl ?? null,
      expiresAt:   build.expirationDate ?? null,
      repoUrl:     repoUrl || null,
      error:       null,
      logsUrl:     `https://expo.dev/accounts/${EXPO_OWNER}/projects/builds/${buildId}`,
    };
  });
}

export async function getMobileBuildStatus(buildId: string): Promise<MobileBuildStatus> {
  const token = process.env.EXPO_TOKEN;
  if (!token) throw new Error("EXPO_TOKEN not set");

  const res = await fetch(`https://api.expo.dev/v2/builds/${buildId}`, {
    headers: { Authorization: `Bearer ${token}`, "Content-Type": "application/json" },
  });
  const text = await res.text();
  if (!res.ok) throw new Error(`EAS status ${res.status}: ${text.slice(0, 300)}`);

  const data  = JSON.parse(text);
  const build = data?.data ?? data;

  return {
    buildId,
    status:      build.status ?? "unknown",
    platform:    build.platform?.toLowerCase() ?? "android",
    artifactUrl: build.artifacts?.applicationArchiveUrl ?? null,
    expiresAt:   build.expirationDate ?? null,
    error:       build.error?.message ?? null,
    logsUrl:     build.logsPageUrl ?? `https://expo.dev/accounts/${EXPO_OWNER}/projects/builds/${buildId}`,
  };
}
